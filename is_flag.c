#include "include/ft_printf.h"
#include "libft/libft.h"

int	is_flag(int c)
{
	int	tmp_cnd;

	tmp_cnd = (c == '.' || c == '-' || c == '#' || c == '+');
	return (tmp_cnd || c == ' ' || ft_isdigit(c));
}
